var x = 0, y = 5;
switch (x - y) {
    case 0:
        console.log("Result is zero");
        break;
    case 5:
        console.log("Result is five");
        break;
    case 10:
        console.log("Result is ten");
        break;
    default:
        console.log("Result is something else");
        break;
}
